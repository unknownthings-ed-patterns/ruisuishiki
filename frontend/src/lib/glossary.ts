/**
 * 用語辞書（公式の景色や問題文で使う前提知識への跳び方）。
 *
 * derivation や問題文の中で [用語名] と書くと、自動で辞書を引いて
 * ツールチップ → 詳細パネル（易しい例・図・関連系列）を段階的に開ける。
 *
 * 辞書にない用語は素通し（表示は変わらない）。
 *
 * ## 設計方針
 *
 * - **short**：1 文の超簡易説明。ホバーで即見られる
 * - **easy**：易しい例で噛み砕いた説明。「もっと詳しく」を押すと開く
 * - **figureMarker**：必要なら図のマーカー（components/Math.tsx の SVG）
 * - **relatedSeriesId**：もっと体系的に学べる関連系列
 *
 * ## ビジョン
 *
 * 小学生でもやる気のある子が、リンクをたどって高校・大学数学まで
 * 登っていけるよう、用語の説明は易しい例から始める。
 * 「下から見ても登れる、上から見ても降りられる」知識のグラフ。
 *
 * relatedSeriesId は学習者ビューの seriesId を直接指す。
 * 新しい系列を追加したら、関連する用語の relatedSeriesId も更新する。
 */

export type GlossaryEntry = {
  /** 1 文の超簡易説明（ホバーで見える） */
  short: string;
  /**
   * 易しい例で噛み砕いた説明（「もっと詳しく」で開ける）。
   * 旧フォーマット：1ブロックで定義・例を一気に書く。
   * meaning が設定されているエントリでは無視される。
   * markdown 風： **強調**、$...$（インライン数式）、$$...$$（ディスプレイ数式）、<<図マーカー>> 対応。
   */
  easy?: string;
  /**
   * 構造化された辞書ページ。ワークシート方式（教師実践に由来）：
   *   §1 meaning   定義（説明）と例
   *   §2 figures   図や絵などをかいてみよう
   *   §3 scenes    使う場面 / 生活の中では？
   *   §4 relatedTerms にていることば / なかまのことば
   * これらのフィールドのうち meaning が設定されていれば構造化レイアウトで描画する。
   * 空のセクションは省略される（書けていない＝書く対象が見える）。
   */
  meaning?: string;
  figures?: string;
  scenes?: string;
  relatedTerms?: string;
  /** もっと体系的に学べる関連系列のID（公式の景色を持つ系列） */
  relatedSeriesId?: string;
  /**
   * 「自分で例を作って確かめる」インタラクティブセクション。
   * 結城浩さんのパタン「例示は理解の試金石」を実装する核。
   * 受動的な「読む」から能動的な「作る」への橋渡し。
   */
  example?: ExampleSpec;
};

/** 入力フィールドの仕様 */
export type ExampleInput = {
  /** 内部キー（verify 関数の引数）。例: "a", "b", "c" */
  name: string;
  /** 表示ラベル。例: "a"、"a（短辺）" */
  label: string;
};

/** 検証結果 */
export type VerifyResult = {
  ok: boolean;
  /** 計算過程の詳細。「a² + b² = 25、c² = 25」など。常に表示 */
  detail: string;
  /** 評価メッセージ。「一致！」「惜しい！」など。常に表示 */
  message: string;
  /** 失敗時の前向きなヒント。失敗時のみ表示 */
  hint?: string;
  /** 重複判定キー。「3,4,5」のように同じ例を識別する */
  canonicalKey: string;
  /** 自明な例（0だけなど）か。控えめなフィードバックにする */
  trivial?: boolean;
};

/** 「自分で例を作ってみる」セクションの仕様 */
export type ExampleSpec = {
  /** プロンプト文。入力欄の上に表示 */
  prompt: string;
  /** 入力フィールド群 */
  inputs: ExampleInput[];
  /** 検証関数。入力値から結果を返す */
  verify: (values: Record<string, number>) => VerifyResult;
};

